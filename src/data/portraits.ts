export function getPortrait(id: PortraitId): PortraitDef {
  return PORTRAITS[id]
}

export function portraitsByTone(tone: PortraitTone | 'all'): PortraitDef[] {
  const all = PLAYLIST.map((id) => PORTRAITS[id])
  if (tone === 'all') return all
  return all.filter((p) => p.tone === tone)
}
